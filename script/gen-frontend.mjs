/**
 * Generate the frontend's typed contract layer from the Hardhat build artifacts + the
 * latest testnet deployment. Re-run after every `pnpm deploy:testnet` so addresses + ABIs
 * never drift from what's on-chain.
 *
 *   node script/gen-frontend.mjs
 *
 * Writes frontend/src/lib/generated.ts: `addresses` (from deployments/somniaTestnet.json) and
 * `abis` (from artifacts/), both `as const` so viem/wagmi infer exact types.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

// contract name -> artifact path (relative to artifacts/)
const CONTRACTS = {
  SentinelRegistry: "src/SentinelRegistry.sol/SentinelRegistry.json",
  SentinelPool: "src/SentinelPool.sol/SentinelPool.json",
  SentinelPolicy: "src/SentinelPolicy.sol/SentinelPolicy.json",
  SentinelTreasury: "src/SentinelTreasury.sol/SentinelTreasury.json",
  SentinelOracle: "src/SentinelOracle.sol/SentinelOracle.json",
  PriceFeedPoller: "src/PriceFeedPoller.sol/PriceFeedPoller.json",
  MockPriceOracle: "src/mocks/MockPriceOracle.sol/MockPriceOracle.json",
  MockStable: "src/mocks/MockStable.sol/MockStable.json",
};

const deployment = JSON.parse(
  readFileSync(join(root, "deployments", "somniaTestnet.json"), "utf8"),
);

const abis = {};
for (const [name, rel] of Object.entries(CONTRACTS)) {
  const art = JSON.parse(readFileSync(join(root, "artifacts", rel), "utf8"));
  abis[name] = art.abi;
}

const banner =
  "// AUTO-GENERATED by script/gen-frontend.mjs — do not edit by hand.\n" +
  "// Re-run `node script/gen-frontend.mjs` after each deploy to resync addresses + ABIs.\n";

const body =
  banner +
  "\n" +
  `export const chainId = ${deployment.chainId} as const;\n\n` +
  `export const deployment = ${JSON.stringify(
    {
      chainId: deployment.chainId,
      subscriptionId: deployment.subscriptionId,
      demoTokenId: deployment.demoTokenId,
      issuerJsonUrl: deployment.issuerJsonUrl,
      issuerPageUrl: deployment.issuerPageUrl,
      issuerSocialUrl: deployment.issuerSocialUrl,
      monitor: deployment.monitor ?? null,
    },
    null,
    2,
  )} as const;\n\n` +
  `export const addresses = ${JSON.stringify(deployment.contracts, null, 2)} as const;\n\n` +
  `export const stables = ${JSON.stringify(deployment.stables ?? [], null, 2)} as const;\n\n` +
  `export const demoTokens = ${JSON.stringify(deployment.demoTokens ?? {}, null, 2)} as const;\n\n` +
  `export const abis = {\n` +
  Object.entries(abis)
    .map(([name, abi]) => `  ${name}: ${JSON.stringify(abi)} as const,`)
    .join("\n") +
  `\n} as const;\n`;

const out = join(root, "frontend", "src", "lib", "generated.ts");
writeFileSync(out, body);
console.log(`Wrote ${out}`);
console.log(`  chainId ${deployment.chainId}, ${Object.keys(abis).length} ABIs`);
console.log(`  oracle ${deployment.contracts.oracle}`);
